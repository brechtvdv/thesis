$( document ).ready(function() {
	// Load stops inside Chosen list
	$.each(stops, function(key, value) {
			$('.stopvan').append("<option value='"+key+"'>"+value.stop_name+"</option>");
	});
	$(".stopvan").trigger("chosen:updated");
	$.each(stops, function(key, value) {
			$('.stopnaar').append("<option value='"+key+"'>"+value.stop_name+"</option>");
	});
	$(".stopnaar").trigger("chosen:updated");
});
